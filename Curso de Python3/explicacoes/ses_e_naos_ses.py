
# IF = SE
# "Papai, você comprou pão?"
# se sim ou se não
# se comprou, ele vai agradecer
# Se não comprou, ele vai chorar
# elif - else if - Se não, outra coisa
# else - senão, sem resposta

resposta = input('Papai, você comprou pão?    ')

if resposta == 'sim':
    print('Obrigado, Papai')
elif resposta == 'não':
    print('Estou chorando, buá, buá')
else:
    print('Não foi o que eu perguntei')
